package en.via.sep3_t3.domain;

public class HousePicture
{
  private int id;
  private String path;

  public void setId(int id)
  {
    this.id = id;
  }

  public void setPath(String path)
  {
    this.path = path;
  }

  public int getId()
  {
    return id;
  }

  public String getPath()
  {
    return path;
  }
}
